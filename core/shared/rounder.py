def rounder(value: str) -> float | str:
    """
    Преобразует строку в число с округлением до двух знаков.

    Пытается преобразовать строковое значение в число с плавающей точкой.
    В случае успеха округляет его до двух десятичных знаков.
    Если преобразование невозможно, возвращает исходную строку.

    Args:
        value: Строковое значение для преобразования

    Returns:
        float | str: Округленное число или исходная строка

    Example:
        >>> rounder("5.678")
        5.68
        >>> rounder("10.0")
        10.0
        >>> rounder("not_a_number")
        'not_a_number'
        >>> rounder("")  # Пустая строка
        ''

    Note:
        Функция используется при обработке результатов анализаторов,
        где некоторые значения могут быть строками (например, ">10",
        "<0.1", "Normal").
    """
    try:
        floated_value = float(value)
    except (ValueError, TypeError):
        # Если значение не может быть преобразовано в число,
        # возвращаем его как есть
        return value

    return round(floated_value, 2)