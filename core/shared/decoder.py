def decode_message(message: bytes) -> str:
    """
    Декодирует байтовое сообщение в строку UTF-8.

    Преобразует входящие байтовые данные в строку для дальнейшей
    обработки парсером.

    Args:
        message: Байтовое представление сообщения

    Returns:
        str: Декодированная строка в кодировке UTF-8

    Raises:
        UnicodeDecodeError: Если сообщение содержит некорректные
                           байты для UTF-8

    Example:
        >>> msg_bytes = b"MSH|^~\\\\&|LAB|..."
        >>> decoded = decode_message(msg_bytes)
        >>> print(decoded)
        'MSH|^~\\&|LAB|...'

    Note:
        Кодировка UTF-8 используется по умолчанию, так как HL7-сообщения
        обычно передаются в этой кодировке. При необходимости можно
        расширить функцию для поддержки других кодировок.
    """
    return message.decode("utf-8")
